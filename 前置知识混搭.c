#include <stdio.h>

/**
 * 交换两个变量的值（注意：这个版本实际上不会生效）
 * 因为C语言是值传递，函数内修改不会影响外部变量
 * 后续学习指针后会解决这个问题
 */
void swap(int n1, int n2) {
    int temp = n2;
    n2 = n1;
    n1 = temp;
    // 这里的修改只在函数内部有效
}

int main() {
    // 常量声明 - 使用const关键字，不可二次赋值
    const double PI = 3.14;
    
    // 变量声明与初始化
    int n1 = 0; // 声明并初始化
    int n2;     // 只声明未初始化
    
    // 输入输出
    printf("请输入两个整数（用空格分隔）: ");
    scanf("%d %d", &n1, &n2); // 注意：scanf需要变量的地址（使用&符号）
    
    // C99允许在代码块中任意地方声明变量
    int n3 = 100 - n1;
    
    // 算术运算和输出
    printf("%d * %d = %d\n", n1, n2, n1 * n2);
    
    // 类型转换和浮点数运算
    // %lf用于double类型，%f用于float类型
    printf("%lf, %f \n", (n1 + PI / 12) * 0.3038, (double)n1 / 2.0);
    
    // 函数调用 - 尝试交换n1和n2的值（但不会实际生效）
    printf("交换前: n1=%d, n2=%d\n", n1, n2);
    swap(n1, n2); //现阶段函数只能赋值 
    printf("交换后: n1=%d, n2=%d (注意：值没有真正交换)\n", n1, n2);
    
    // 复合赋值运算符
    // 注意：n1 *= n2 + 1 等价于 n1 = n1 * (n2 + 1)
    n1 *= n2 + 1;
    printf("n1 *= n2 + 1 后: n1=%d\n", n1);
    
    // 自增运算符
    n1++;    // 先使用n1的值，然后再增加1
    ++n1;    // 先增加1，然后再使用n1的值
    printf("两次自增后: n1=%d\n", n1);
    
    return 0;
}
